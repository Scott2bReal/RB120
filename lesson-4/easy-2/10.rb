# What are the benefits of using Object Oriented Programming in Ruby? Think of
# as many as you can.
#
# The benefits of using OOP in Ruby are that it allows the use of encapsulation
# to only expose certain parts of elements of a program to each other, which
# allows for more complex and larger programs to be maintained more easily. It
# also allows for polymorphism, which simplifies the interface of the different
# objects involved in a program. Because different data types can respond
# differently to a common interface, the inner workings of those objects don't
# need to be exposed to the programmer.
#
# OOP also makes a large project easier to conceptualize and design, as using
# real world objects, relationships, and behaviors is easier to understand than
# more esoteric computing concepts.
