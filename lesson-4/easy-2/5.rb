# There are a number of variables listed below. What are the different types and
# how do you know which is which?

excited_dog = "excited dog"
@excited_dog = "excited dog"
@@excited_dog = "excited dog"

# The first is a local variable, the second is an instance variable, and the
# third is a class variable. Local variables do not have an @ symbol, instance
# variables start with one @ symbol, and class variables start with two @@
# symbols
